import axios from 'axios';

import { IDictionaryHasWord } from '@/shared/model/dictionary-has-word.model';

const baseApiUrl = 'api/dictionary-has-words';

export default class DictionaryHasWordService {
  public find(id: number): Promise<IDictionaryHasWord> {
    return new Promise<IDictionaryHasWord>((resolve, reject) => {
      axios
        .get(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public retrieve(): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .get(baseApiUrl)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public delete(id: number): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .delete(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public create(entity: IDictionaryHasWord): Promise<IDictionaryHasWord> {
    return new Promise<IDictionaryHasWord>((resolve, reject) => {
      axios
        .post(`${baseApiUrl}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public update(entity: IDictionaryHasWord): Promise<IDictionaryHasWord> {
    return new Promise<IDictionaryHasWord>((resolve, reject) => {
      axios
        .put(`${baseApiUrl}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }
}
